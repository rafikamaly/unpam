    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtNama.getText().isEmpty() || txtUsia.getText().isEmpty()) {
            lblHasil.setText("Data tidak lengkap !");
            return;
        }
        
        String gender = "";
        if (rbLaki.isSelected()) {
            gender = "Laki-laki";
        } else if (rbPerempuan.isSelected()) {
            gender = "Perempuan";
        } else {
                lblHasil.setText("Pilih jenis kelamin !");
                return;
        }
        
        String kursus = cmbKursus.getSelectedItem().toString();
        
        Peserta p = new Peserta();
        p.setData(txtNama.getText(),
                  Integer.parseInt(txtUsia.getText()),
                  gender, kursus);
        
        data[index++] = p;
        
        txtArea.append(p.getInfo() + "\n");
    }//GEN-LAST:event_jButton1ActionPerformed

