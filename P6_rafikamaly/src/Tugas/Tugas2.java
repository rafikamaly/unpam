    public class Peserta {
        private String nama;
        private int usia;
        private String gender;
        private String kursus;
        
        public void setData(String nama, int usia, String gender, String kursus) {
            this.nama = nama;
            this.usia = usia;
            this.gender = gender;
            this.kursus = kursus;
        }
        
        public String getInfo() {
            String info = nama + " | " + usia + "thn | " + gender + " | " + kursus;
            return info;
        }
        
    }
