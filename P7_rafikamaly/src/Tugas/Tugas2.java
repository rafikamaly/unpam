    public class Laundry {
        private String nama;
        private double berat;
        private String layanan;
        private String paket;
        
        public Laundry(String nama, double berat, String layanan, String paket) {
            this.nama = nama;
            this.berat = berat;
            this.layanan = layanan;
            this.paket = paket;
        }
        
        public double getTotal() {
            double hargaPerKg;
            
            if (layanan.equals("Cuci")) {
                hargaPerKg = 5000;
            } else {
                hargaPerKg = 8000;
            }
            
            if (paket.equals("Express")) {
                hargaPerKg += 3000;
            }
            
            return berat * hargaPerKg;
        }
        
        public String getInfo() {
            String info = nama + " | " + layanan + " | " + paket + " | " + berat + " kg";
            return info;
        }
    }
    
