    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtNama.getText().isEmpty() || txtBerat.getText().isEmpty()) {
            txtArea.setText("Data tidak lengkap !");
            return;
        }
        
        String layanan = "";
        
        if (rbCuci.isSelected()) {
            layanan = "Cuci";
        } else if (rbCuciSetrika.isSelected()) {
            layanan = "Cuci + Setrika";
        } else {
            txtArea.setText("Pilih layanan!\n");
            return;
        }
        
        String paket = cmbPaket.getSelectedItem().toString();
        double berat = Double.parseDouble(txtBerat.getText());
        
        Laundry l = new Laundry(txtNama.getText(), berat, layanan, paket);
        data[index++] = l;
        
        txtArea.append("Data: " + l.getInfo() + "\n");
        
        txtNama.setText("");
        txtBerat.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (index == 0) {
            txtArea.setText("Belum ada data");
            return;
        }
        
        Laundry last = data[index-1];
        double total = last.getTotal();
        
        txtArea.append("\nPerhitungan Laundry\n");
        txtArea.append("Nama: " + last.getInfo() + "\n");
        txtArea.append("Total: Rp " + total + "\n");
    }//GEN-LAST:event_jButton2ActionPerformed

