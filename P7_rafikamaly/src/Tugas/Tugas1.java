    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if (txtNama.getText().isEmpty() || txtJam.getText().isEmpty()) {
            txtArea.setText("Data tidak lengkap !");
        }
        
        String status = "";
        
        if (rbTetap.isSelected()) {
            status = "Tetap";
        } else if (rbKontrak.isSelected()) {
            status = "Kontrak";
        } else {
            txtArea.setText("Pilih status !");
            return;
        }
        
        String shift = cmbShift.getSelectedItem().toString();
        int jam = Integer.parseInt(txtJam.getText());
        
        Karyawan k = new Karyawan(txtNama.getText(), jam, status, shift);
        data[index++] = k;
        
        txtArea.append("Data: " + k.getInfo() + "\n");
        
        txtNama.setText("");
        txtJam.setText("");
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        if (index == 0) {
            txtArea.setText("Belum ada data !");
            return;
        }
        
        Karyawan last = data[index-1];
        int gaji = last.getGaji();
        
        txtArea.setText("Perhitungan Gaji:\n");
        txtArea.setText("Nama: " + last.getInfo() + "\n");
        txtArea.setText("Gaji: Rp " + gaji + "\n");
    }//GEN-LAST:event_btnHitungActionPerformed

