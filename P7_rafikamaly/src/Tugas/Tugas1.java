    public class Karyawan {
        private String nama;
        private int jam;
        private String status;
        private String shift;
        
        public Karyawan(String nama, int jam, String status, String shift) {
            this.nama = nama;
            this.jam = jam;
            this.status = status;
            this.shift = shift;
        }
        
        public int getGaji() {
            int tarif = status.equals("Tetap") ? 20000 : 15000;
            
            if (shift.equals("Malam")) {
                tarif += 5000;
            }
            
            int gaji;
            
            if (jam <= 8) {
                gaji = jam * tarif;
            } else {
                int lembur = jam - 8;
                gaji = (8 * tarif) + (int)(lembur * tarif * 1.5);
            }
            
            if (jam >= 20) {
                gaji += 50000;
            }
            
            return gaji;
        }
        
        public String getInfo() {
            String info = nama + " | " + status + " | " + shift + " | " + jam + " jam";
            return info;
        }
        
    }
